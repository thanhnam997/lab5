// JavaScript conditional if...else

let a = 5;
let b = 10;

// Case 1
if (a < b) {
    console.log('condition a < b');
}

// Case 2
if (a > b) {
    console.log('condition a > b');
}

// Case 3
if (a > b) {
    console.log('condition a > b');
} else {
    console.log('condition a > b is not met');
}

// Case 4
if (a > b) {
    console.log('condition a > b');
} else if (a < b) {
    console.log('condition a > b is not met but condition a < b');
} else {
    console.log('the above condition is not met');
}
